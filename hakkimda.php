<font color="red">  <u>  <i>  <h2>  HAKKIMIZDA  </h2>  </i>  </u>  </font>
<p> <i> Kendisini fark yaratacak eğitim ve araştırmaya adamış başarılı akademik kadrosu ile İstinye Üniversitesi, öğrencilerine var 
olan bilgiyi aktararak kendi alanlarında güçlü bir donanım sağlarken, eğitim ve araştırma performansı ile yeni bilgi üretilmesine 
katkıda bulunarak Türkiye’nin ve dünyanın seçkin üniversiteleri arasında yer almayı hedeflemektedir.Öğrenci odaklı eğitim anlayışını 
üniversitenin tüm süreçlerinde uygulayan İstinye Üniversitesi, bilim ve araştırma merkezi olma vizyonu doğrultusunda, öğretim üyelerinin 
araştırmaları ile bilimin sınırlarını genişletmeyi, bilimsel gelişmelerden edinilen bulguları toplumun refahı için uygulamaya geçirmeyi 
ve bununla birlikte topluma kaliteli ve erişilebilir sağlık hizmeti sunmayı amaçlamaktadır. Öğretim, araştırma ve toplum hizmetleri 
etkinliklerini evrensel standartlarda yürüterek, öğrencilerine geniş tabanlı bir bilgi yelpazesinin yanı sıra, teknoloji ve sanatı da 
kapsayan bir öğrenme ve ilerleme ortamı sunar. Öğrencilerini liderlik yetenekleri ile donatılmış, bilimin gücüne güvenen, dünyadaki 
gelişmeleri yakından takip eden, eleştirel düşünebilen; insani ve etik değerlere sahip, kendini geliştirme ve yaratıcılığını kullanma 
alışkanlığı edinmiş bireyler olarak yetiştirir. İstinye Üniversitesi, geleceğin yetkin, yaratıcı ve ileri görüşlü bireylerini bünyesinde 
yetiştirerek toplumumuzun sosyal, kültürel, ekonomik, bilimsel ve teknolojik gelişiminde önemli bir rol oynamayı ve insanlığın 
ilerlemesine katkıda bulunmayı hedeflemektedir.
</i> </p>

